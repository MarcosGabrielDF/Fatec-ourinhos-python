# Entrada
print('===== Vamos calcular o valor de f(x) = 2x + 3 =====')
print('')
x = float(input('Digite o valor de x: '))

# Cálculo
f_de_x = 2 * x + 3

# Saída
print(f'O valor de f(x) = {f_de_x:g}')
