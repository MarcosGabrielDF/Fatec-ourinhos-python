# Perguntas
prova1 = float(input('Informe a 1ª nota: '))
prova2 = float(input('Informe a 2ª nota: '))

# Cálculo
nota1 = prova1 * 0.4
nota2 = prova2 * 0.6
media = (nota1 + nota2)

# Exibição do resultado
print(f'A média desse aluno é {media:.2f}')

